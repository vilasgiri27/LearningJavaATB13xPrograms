package ex_03_Java_Variable;

public class Lab026_Relational
{
    public static void main(String[] args) {

        int a=10;
        int b=20;

        System.out.println("This is relational operator");
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a!=b);
        System.out.println(a==b);

        System.out.println("Thank you...");
    }
}
